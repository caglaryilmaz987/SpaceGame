public class Substance {
    // Every derived object must have x and y coordinates, so I preferred to maintain this property at the top level

    protected int x, y;

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

}
